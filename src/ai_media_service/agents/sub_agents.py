# ── Provider routing ──────────────────────────────────────────────────────────
# ChiefAgent  → Groq  (llama-3.3-70b,  100K tokens/day, function calling)
# Sub-agents  → Google Gemini 1.5 Flash (FREE, 1M tokens/day)
# Fallback    → Groq  (auto, если GOOGLE_API_KEY не задан)

_GEMINI_ATTRS = {
    "_api_key_attr":  "GOOGLE_API_KEY",
    "_base_url_attr": "GOOGLE_BASE_URL",
    "_model_attr":    "GOOGLE_MODEL",
    "_provider_name": "google",
}


def _gemini(cls):
    """Декоратор: переключает агента на Google Gemini."""
    for attr, val in _GEMINI_ATTRS.items():
        setattr(cls, attr, val)
    return cls


